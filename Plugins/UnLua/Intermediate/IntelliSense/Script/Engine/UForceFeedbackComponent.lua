---ForceFeedbackComponent allows placing a rumble effect in to the world and having it apply to player characters who come near it
---@class UForceFeedbackComponent : USceneComponent
---@field public ForceFeedbackEffect UForceFeedbackEffect @The feedback effect to be played
---@field public bAutoDestroy boolean @Auto destroy this component on completion
---@field public bStopWhenOwnerDestroyed boolean @Stop effect when owner is destroyed
---@field public bLooping boolean
---@field public bIgnoreTimeDilation boolean @Should the playback of the forcefeedback pattern ignore time dilation and use the app's delta time
---@field public bOverrideAttenuation boolean @Should the Attenuation Settings asset be used (false) or should the properties set directly on the component be used for attenuation properties
---@field public IntensityMultiplier number @The intensity multiplier to apply to effects generated by this component
---@field public AttenuationSettings UForceFeedbackAttenuation @If bOverrideSettings is false, the asset to use to determine attenuation properties for effects generated by this component
---@field public AttenuationOverrides FForceFeedbackAttenuationSettings @If bOverrideSettings is true, the attenuation properties to use for effects generated by this component
---@field public OnForceFeedbackFinished MulticastDelegate @called when we finish playing audio, either because it played to completion or because a Stop() call turned it off early
local UForceFeedbackComponent = {}

---Stop playing the feedback effect
function UForceFeedbackComponent:Stop() end

---Set a new intensity multiplier
---@param NewIntensityMultiplier number
function UForceFeedbackComponent:SetIntensityMultiplier(NewIntensityMultiplier) end

---Set what force feedback effect is played by this component
---@param NewForceFeedbackEffect UForceFeedbackEffect
function UForceFeedbackComponent:SetForceFeedbackEffect(NewForceFeedbackEffect) end

---Start a feedback effect playing
---@param StartTime number @[opt] 
function UForceFeedbackComponent:Play(StartTime) end

---@param OutAttenuationSettings FForceFeedbackAttenuationSettings @[out] 
---@return boolean
function UForceFeedbackComponent:BP_GetAttenuationSettingsToApply(OutAttenuationSettings) end

---Modify the attenuation settings of the component
---@param InAttenuationSettings FForceFeedbackAttenuationSettings
function UForceFeedbackComponent:AdjustAttenuation(InAttenuationSettings) end

