---Used to describe an element that can be linked to a segment in a montage or sequence.
---   Usage:
---           Inherit from FAnimLinkableElement and make sure to call LinkMontage or LinkSequence
---           both on creation and on loading the element. From there SetTime and GetTime should be
---           used to control where this element is in the montage or sequence.
---           For more advanced usage, see this implementation used in FAnimNotifyEvent where
---           we have a secondary link to handle a duration
---           @@see FAnimNotifyEvent
---@class FAnimLinkableElement
---@field protected LinkedMontage UAnimMontage @The montage that this element is currently linked to
---@field protected SlotIndex integer @The slot index we are currently using within LinkedMontage
---@field protected SegmentIndex integer @The index of the segment we are linked to within the slot we are using
---@field protected LinkMethod integer @The method we are using to calculate our times
---@field protected CachedLinkMethod integer @Cached link method used to transform the time when LinkMethod changes, always relates to the currently stored time
---@field protected SegmentBeginTime number @The absolute time in the montage that our currently linked segment begins
---@field protected SegmentLength number @The absolute length of our currently linked segment
---@field protected LinkValue number @The time of this montage. This will differ depending upon the method we are using to link the time for this element
---@field protected LinkedSequence UAnimSequenceBase @The Animation Sequence that this montage element will link to, when the sequence changes in either length or rate; the element will correctly place itself in relation to the sequence
